package dio.guilherme.model;

public class Calculadora {

    public static int soma(int x,int y){
        return x + y;
    }

    public static int subtracao(int x, int y){
        return x-y;
    }

    public static int mult(int x, int y){
        return x*y;
    }

    public static int div(int x ,int y){
        return x/y;
    }
  
}
